    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ControladorBanco ctrl = new ControladorBanco();
        
        for(Produto p: ctrl.buscarProduto()){
            modelo.addRow(new Object[]{
                p.getNome(),
                p.getValor(),
                p.getCategoria(),
                p.getPerecivel()
            });
        }
    }

