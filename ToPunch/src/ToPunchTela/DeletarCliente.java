     public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ControladorBanco ctrl = new ControladorBanco();
        
        for(Cliente cli: ctrl.buscarCliente(txtCPF.getText())){
            modelo.addRow(new Object[]{
                cli.getNome(),
                cli.getEndereco(),
                cli.getTelefone(),
                cli.getCpf(),
                cli.getCep(),
                cli.getDataNasc()
                
            });
        }
    }
